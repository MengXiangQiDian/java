package day02;

public class HomeWork03 {
    public static void main(String[] args) {
        //保存两本书名，用+拼接，保存两个性别
        String book1 = "天龙八部";
        String book2 = "笑傲江湖";
        System.out.println(book1 + book2);//天龙八部笑傲江湖

        char c1 = '男';
        char c2 = '女';
        System.out.println(c1 + c2);//得到一个整数

        //保存两本书的价格
        double price1 = 123.45;
        double price2 = 100.11;
        System.out.println(price1 + price2); //123.45+100.11
    }
}
